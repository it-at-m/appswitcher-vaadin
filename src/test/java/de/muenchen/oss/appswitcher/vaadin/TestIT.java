package de.muenchen.oss.appswitcher.vaadin;

import org.junit.jupiter.api.AfterAll;
import org.junit.jupiter.api.AfterEach;
import org.junit.jupiter.api.BeforeAll;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;

import com.microsoft.playwright.Browser;
import com.microsoft.playwright.BrowserContext;
import com.microsoft.playwright.BrowserType;
import com.microsoft.playwright.Frame;
import com.microsoft.playwright.Locator;
import com.microsoft.playwright.Page;
import com.microsoft.playwright.Playwright;
import com.microsoft.playwright.assertions.PlaywrightAssertions;

class TestIT {

    // Shared between all tests in this class.
    static Playwright playwright;
    static Browser browser;

    // New instance for each test method.
    BrowserContext context;
    Page page;

    @Test
    void test() throws InterruptedException {
        System.out.println("START");
        page.navigate("http://localhost:8080");
        System.out.println(page.title());
        Locator locator = page.locator("css=[id='appswitcher-component']");
        PlaywrightAssertions.assertThat(locator).isVisible();
        locator.click();
        Thread.sleep(3000);
        Frame appswitcherFrame = page.frame("appswitcher-iframe");
        Locator kalenderApp = appswitcherFrame.getByText("Kalender");
        kalenderApp.click();
        Thread.sleep(3000);
    }

    @BeforeAll
    static void launchBrowser() {
        playwright = Playwright.create();
        browser = playwright.chromium().launch(new BrowserType.LaunchOptions().setHeadless(false));
    }

    @AfterAll
    static void closeBrowser() {
        playwright.close();
    }

    @BeforeEach
    void createContextAndPage() {
        context = browser.newContext();
        page = context.newPage();
    }

    @AfterEach
    void closeContext() {
        context.close();
    }

}
